    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $directors = Director::find($id);
        $directors->delete();

        // redirect
        Session::flash('message', 'Director Successfully deleted!');
